# assoziative Listen / Dictionary

hash = { 'toll':1, 'gut':2, 'ok':3, 'geht noch':4, 'schlecht':5, 'extrem schlecht':6 }

print(hash.keys()) # dict_keys(['toll', 'gut', 'ok', 'geht noch', 'schlecht', 'extrem schlecht'])
print(hash.values()) # dict_values([1, 2, 3, 4, 5, 6])
print(hash.items()) # dict_items([('toll', 1), ('gut', 2), ('ok', 3), ('geht noch', 4), ('schlecht', 5), ('extrem schlecht', 6)])
